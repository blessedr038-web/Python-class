#Arithmetic Operators

x = 10
y = 5
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus - Returns remainder


#Comparison Operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) #Equal to
print(x != y) #Not equal


#Assignment Operators
a = 56
print(a)


#Operator Precedence
a += 2
print(a)

# Logical Operations - and, or, not
print(100 < 200 and 100 == 200)
print(100 < 200 or 100 == 200)
print( not(100 < 200 or 100 == 200))


